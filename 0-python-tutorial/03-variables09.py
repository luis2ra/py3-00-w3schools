# Demo Python Variables - Assign Multiple Values
'''
Python Variables - Many Values to Multiple Variables


Python allows you to assign values to multiple variables in one line.

Note: Make sure the number of variables matches the number of values, or else you will get an error (ValueError).

'''
x, y, z = "Orange", "Banana", "Cherry"

print(x)
print(y)
print(z)
