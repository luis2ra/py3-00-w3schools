# Demo Python For Loops - The pass Statement
'''
The pass Statement

"for" loops cannot be empty, but if you for some reason have a "for" loop with 
no content, put in the "pass" statement to avoid getting an error.

'''
# Example:
for x in [0, 1, 2]:
    pass
