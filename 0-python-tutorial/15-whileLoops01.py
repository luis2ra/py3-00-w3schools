# Demo Python While Loops
'''
Python While Loops


Python Loops

Python has two primitive loop commands:

* "while" loops
* "for" loops


The while Loop

With the "while" loop we can execute a set of statements as long as a condition is true.

'''
# Print i as long as i is less than 6:
i = 1

while i < 6:
    print(i)
    i += 1  # Note: remember to increment i, or else the loop will continue forever.

# Note: The "while" loop requires relevant variables to be ready, in this 
# example we need to define an indexing variable, i, which we set to 1.
