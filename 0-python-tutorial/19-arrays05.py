# Demo Python Arrays - Looping Array Elements
'''
Looping Array Elements

You can use the "for in" loop to loop through all the elements of an array.

'''
# Print each item in the cars array:
cars = ["Ford", "Volvo", "BMW"]

for x in cars:
    print(x)
