# Demo Python For Loops - The range() Function
'''
The range() Function - Part 2

The range() function defaults to 0 as a starting value, however it is possible to specify 
the starting value by adding a parameter: range(2, 7), which means values from 2 to 7 (but not including 7):

'''
# Using the start parameter:
for x in range(2, 7):
    print(x)
print("the end...")
