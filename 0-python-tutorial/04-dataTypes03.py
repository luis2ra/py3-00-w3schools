# Demo Python Data Types - Setting the Specific Data Type
'''
Setting the Specific Data Type

If you want to specify the data type, you can use the following constructor functions.

'''
# If you want to specify the data type, you can use the following constructor functions:
x1 = str("Hello World")

#display x1:
print(x1)

#display the data type of x1:
print(type(x1), '\n')

x2 = int(20)

#display x2:
print(x2)

#display the data type of x2:
print(type(x2), '\n')


x3 = float(20.5)

#display x3:
print(x3)

#display the data type of x3:
print(type(x3), '\n')


x4 = complex(1j)

#display x4:
print(x4)

#display the data type of x4:
print(type(x4), '\n')


x5 = list(("apple", "banana", "cherry"))

#display x5:
print(x5)

#display the data type of x5:
print(type(x5), '\n')


x6 = tuple(("apple", "banana", "cherry"))

#display x6:
print(x6)

#display the data type of x6:
print(type(x6), '\n')


x7 = range(6)

#display x7:
print(x7)

#display the data type of x7:
print(type(x7), '\n')


x8 = dict(name="John", age=36)

#display x8:
print(x8)

#display the data type of x8:
print(type(x8), '\n')


x9 = set(("apple", "banana", "cherry"))

#display x9:
print(x9)

#display the data type of x9:
print(type(x9), '\n')


x10 = frozenset(("apple", "banana", "cherry"))

#display x10:
print(x10)

#display the data type of x10:
print(type(x10), '\n')


x11 = bool(5)

#display x11:
print(x11)

#display the data type of x11:
print(type(x11), '\n')


x12 = bytes(5)

#display x12:
print(x12)

#display the data type of x12:
print(type(x12), '\n')


x13 = bytearray(5)

#display x13:
print(x13)

#display the data type of x13:
print(type(x13), '\n')


x14 = memoryview(bytes(5))

#display x14:
print(x14)

#display the data type of x14:
print(type(x14), '\n')
