# Demo Python Strings - Slicing Strings
'''
Python Strings - Slicing Strings


Slicing

You can return a range of characters by using the slice syntax.

Specify the start index and the end index, separated by a colon, to return a part of the string.

Note: The first character has index 0.

'''
# Get the characters from position 2 to position 5 (not included):
b = "Hello, World!"
print("string original: ", b)
print("caracteres de la posición 2 a 5 (no lo incluye): ", b[2:5])
print("caracteres de la posición 0 a 2 (no lo incluye): ", b[0:2])
