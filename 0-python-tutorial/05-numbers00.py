'''
Python Numbers


There are three numeric types in Python:

* int
* float
* complex

Variables of numeric types are created when you assign a value to them.

'''
# Variables of numeric types are created when you assign a value to them:
x = 1    # int
y = 2.8  # float
z = 1j   # complex

print(x)
print(y)
print(z)

# To verify the type of any object in Python, use the type() function.
print(type(x))
print(type(y))
print(type(z))
