# Demo Python Dictionaries - Dictionary
'''
Nested Dictionaries

A dictionary can also contain many dictionaries, this is called nested dictionaries.

'''

# Create a dictionary that contain three dictionaries:
myfamily = {
    "child1" : {
        "name" : "Emil",
        "year" : 2004
    },
    "child2" : {
        "name" : "Tobias",
        "year" : 2007
    },
    "child3" : {
        "name" : "Linus",
        "year" : 2011
    }
} 

print(myfamily)