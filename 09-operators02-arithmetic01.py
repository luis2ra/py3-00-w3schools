# Demo Python Operators
'''
Python Arithmetic Operators

Arithmetic operators are used with numeric values to perform common mathematical operations:

Operator 	Name 	            Example
+ 	        Addition 	        x + y 	
-   	    Subtraction 	    x - y 	
* 	        Multiplication 	    x * y 	
/ 	        Division 	        x / y 	
% 	        Modulus 	        x % y 	
** 	        Exponentiation 	    x ** y 	
// 	        Floor division 	    x // y

'''

# Variables
x = 5
y = 3
z = 2

# Addition
print(x + y)

# Subtraction
print(x - y)

# Multiplication
print(x * y)

# Division
print(x / z)

# Modulus
print(x % z)

# Exponentiation
print(x ** y)

# Floor division
print(x // y)      # the floor division // rounds the result down to the nearest whole number
print(x // z)
print( (x * y) // z )     