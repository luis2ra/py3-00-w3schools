'''
Removing Items

There are several methods to remove items from a dictionary.

'''

# The clear() keyword empties the dictionary:
thisdict =	{
    "brand": "TOYOTA",
    "model": "LAND CRUISER",
    "year": 2020,
    "color": "red"
}

print(thisdict)

thisdict.clear()
print(thisdict)